
public class IterationDemo {
	
	// while, do While, for, Enhanced for (for each)

	public static void main(String[] args) {
		
		int i = 8;
		
		/*
		 * while (i<=5) { System.out.println("Hello World"); i++; }
		 */
		
		// While Loop
		
		do {
			System.out.println("Hello World");
			i++;
		}
		while (i<=5); 
		
		for (int j=1;j<=5;j++) {
			System.out.println(j);
		}

	}

}
