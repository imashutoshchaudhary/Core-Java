
public class SelectionStatement {
	
	/*
	 * Selection statemens are 
	 * if .. else
	 * switch
	 * ternary operator
	 */
	
	public static void main(String args[])
	{
		int n = 0;
		
		if (n%2==0) 
		{
			System.out.println("Given No is even number");
		}
		else {
			System.out.println("Given No is odd number");
		}
	}

}
